#include "libft.h"

void	ft_2dchardel(char ***tab)
{
	int	len;
	int	i;

	i = 0;
	len = ft_2dcharlen(*tab);
	while (i < len)
	{
		free((*tab)[i]);
		i++;
	}
	free(*tab);
	*tab = NULL;
}
